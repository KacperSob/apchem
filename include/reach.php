REACH to skrót od <i>Registration, Evaluation, Authorisation and Restriction of Chemicals</i> (Rejestracja, ocena, udzielanie zezwoleń i stosowane ograniczenia w zakresie chemikaliów). Rozporządzenie REACH weszło w życie z dniem 1 czerwca 2007 r.
<br><br>
Jednym z wiodących wymogów REACH jest obowiązek rejestracji substancji chemicznych produkowanych lub importowanych w ilości > 1 tony na rok w postaci własnej, jako składnik mieszaniny oraz składnik wyrobów.
